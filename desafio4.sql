SELECT JOB_TITLE AS 'Cargo',
CAST(AVG(SALARY) AS DECIMAL(10,2)) AS 'Média salarial',
CASE 
WHEN CAST(AVG(SALARY) AS DECIMAL(10,2)) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN CAST(AVG(SALARY) AS DECIMAL(10,2)) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN CAST(AVG(SALARY) AS DECIMAL(10,2)) BETWEEN 7501 AND 10500 THEN 'Sênior'
WHEN CAST(AVG(SALARY) AS DECIMAL(10,2)) > 10500 THEN 'CEO'
END AS 'Senioridade'
FROM hr.jobs job
INNER JOIN hr.employees employee
ON job.JOB_ID = employee.JOB_ID
GROUP BY JOB_TITLE
ORDER BY `Média salarial`, JOB_TITLE;
