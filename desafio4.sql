SELECT JOB_TITLE AS Cargo,
format(AVG(SALARY),2) AS 'Média salarial',
CASE 
WHEN AVG(SALARY) BETWEEN 2000.00 AND 5800.00 THEN 'Júnior'
WHEN AVG(SALARY) BETWEEN 5801.00 AND 7500.00 THEN 'Pleno'
WHEN AVG(SALARY) BETWEEN 7501.00 AND 10500.00 THEN 'Sênior'
WHEN AVG(SALARY) > 10500.00 THEN 'CEO'
END AS Senioridade
FROM hr.employees AS E
INNER JOIN hr.jobs AS J
ON J.JOB_ID = E.JOB_ID
GROUP BY Cargo
ORDER BY AVG(SALARY), Cargo;
