SELECT 
tbJ.JOB_TITLE 'Cargo',
ROUND(AVG(tbE.SALARY), 2) 'Média salarial',
CASE
WHEN ROUND(AVG(tbE.SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN ROUND(AVG(tbE.SALARY), 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN ROUND(AVG(tbE.SALARY), 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END 'Senioridade'
FROM hr.employees tbE
INNER JOIN hr.jobs tbJ
ON tbE.JOB_ID = tbJ.JOB_ID
GROUP BY tbE.JOB_ID
ORDER BY ROUND(AVG(tbE.SALARY), 2), tbJ.JOB_TITLE ASC;
