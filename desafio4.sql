SELECT JOB_TITLE AS 'Cargo', 
MIN_SALARY + MAX_SALARY AS 'Média salarial',
CASE 
WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 5000 AND 5800 THEN 'Júnior'
WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 58001 AND 7500 THEN 'Pleno'
WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 7501 AND 10500 THEN 'Sênior'
WHEN AVG(MIN_SALARY + MAX_SALARY) >  10500 THEN 'CEO'
END AS 'Senioridade'
FROM hr.jobs
GROUP BY JOB_TITLE
ORDER BY `Média Salarial`, `Cargo`;
