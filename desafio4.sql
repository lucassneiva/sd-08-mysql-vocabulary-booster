SELECT j.JOB_TITLE AS `Cargo`, ROUND(AVG(e.SALARY),2) AS `Média salarial`,
CASE
WHEN AVG(ROUND(e.SALARY, 2)) > 10500 THEN 'CEO'
WHEN AVG(ROUND(e.SALARY, 2)) <=10500 AND AVG(ROUND(e.SALARY, 2)) >= 7501 THEN 'Sênior'
WHEN AVG(ROUND(e.SALARY, 2)) <= 7500 AND AVG(ROUND(e.SALARY, 2)) >= 5801 THEN 'Pleno'
WHEN AVG(ROUND(e.SALARY, 2)) <= 5800 AND AVG(ROUND(e.SALARY, 2)) >= 2000 THEN 'Júnior' 
END AS `Senioridade`
FROM hr.employees AS e
INNER JOIN hr.jobs AS j
ON e.JOB_ID = j.JOB_ID
GROUP BY e.JOB_ID
ORDER BY `Média salarial`, j.JOB_TITLE;
