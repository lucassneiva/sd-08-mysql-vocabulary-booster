SELECT 
JOB_TITLE AS `Cargo`,
ROUND(((MIN_SALARY + MAX_SALARY)/2),2) AS `Média Salarial`,
CASE
WHEN ROUND(((MIN_SALARY + MAX_SALARY)/2),2) >= 2000 AND ROUND(((MIN_SALARY + MAX_SALARY)/2),2) <= 5800  THEN 'Júnior'
WHEN ROUND(((MIN_SALARY + MAX_SALARY)/2),2) >= 5801 AND ROUND(((MIN_SALARY + MAX_SALARY)/2),2) <= 7500  THEN 'Pleno'
WHEN ROUND(((MIN_SALARY + MAX_SALARY)/2),2) >= 7501 AND ROUND(((MIN_SALARY + MAX_SALARY)/2),2) <= 10500  THEN 'Sênior'
ELSE 'CEO'
END AS 'Senioridade'
FROM hr.jobs
ORDER BY `Média Salarial`, `Cargo`;
