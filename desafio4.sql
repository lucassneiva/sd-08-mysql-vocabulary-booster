SELECT j.JOB_TITLE AS "Cargo", ROUND(AVG(e.SALARY), 2) AS "Média salarial",
CASE
	WHEN SALARY BETWEEN 2000 AND 5800 THEN "Júnior"
	WHEN SALARY BETWEEN 5801 AND 7500 THEN "Pleno"
	WHEN SALARY BETWEEN 7501 AND 10500 THEN "Sênior"
	WHEN SALARY > 10500 THEN "CEO"
END AS 'Senioridade'
FROM hr.jobs AS j
INNER JOIN employees AS e
ON j.JOB_ID = e.JOB_ID
GROUP BY JOB_TITLE
ORDER BY `Média salarial` ASC, JOB_TITLE ASC;
