SELECT ROUND(AVG(A.SALARY), 2) AS `Média salarial`,
B.JOB_TITLE AS `Cargo`,
CASE
WHEN AVG(SALARY) < 5800 THEN 'Júnior'
WHEN AVG(SALARY) < 7500 THEN 'Pleno'
WHEN AVG(SALARY) <= 10500 THEN 'Sênior'
WHEN AVG(SALARY) > 10500 THEN 'CEO'
END AS `Senioridade`
FROM hr.employees AS A
INNER JOIN hr.jobs AS B
ON A.JOB_ID = B.JOB_ID
GROUP BY B.JOB_ID
ORDER BY `Média salarial`, JOB_TITLE;
