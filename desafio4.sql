SELECT 
tbJ.JOB_TITLE 'Cargo',
ROUND(AVG(tbE.SALARY), 2) 'Média Salarial',
CASE
WHEN AVG(tbE.SALARY) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN AVG(tbE.SALARY) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN AVG(tbE.SALARY) BETWEEN 7501 AND 10500 THEN 'Sênior'
ELSE 'CEO'
END 'Senioridade'
FROM hr.employees tbE
INNER JOIN hr.jobs tbJ
ON tbE.JOB_ID = tbJ.JOB_ID
GROUP BY tbE.JOB_ID
ORDER BY AVG(tbE.SALARY), tbJ.JOB_TITLE ASC;
